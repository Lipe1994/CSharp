namespace Ferreira.Filipe{
    class Loops{
        public static void Main(){
            //loop com for
            for(int index =0; index < 10; index++){
                Console.WriteLine("Valor de index agora é :"+index);
            }

            //loop com while
            int x =0;
            while(x < 10){
                Console.WriteLine("Valor de X agora é :"+x);
                x++;
            }

        }
    }    
}